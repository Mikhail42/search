package org.ionkin.search;

public class P {
    private final String query;
    private final int p1;

    public P(String query, int p1) {
        this.query = query;
        this.p1 = p1;
    }

    public String getQuery() {
        return query;
    }

    public int getP1() {
        return p1;
    }
}
